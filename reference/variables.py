"""
Simple Variables.
"""

# Setting x to 10
x = 10

# Setting y to x
y = x

# Setting z to with some math
z = y/2 + x**x

# Print the variables
print("The first variable is: %s" % x)
print("The second variable is: ", y)
print("The last one is as int is: ", int(z))
print("The last one as float is: %s" % float(z))
print("Variable prints done\n")
